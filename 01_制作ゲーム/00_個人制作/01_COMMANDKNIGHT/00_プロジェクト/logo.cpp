CLogo::CLogo(int nPriority) : CScene(nPriority,OBJTYPE_SCENE2D)
{
	m_pTexture = NULL;							//テクスチャ
	m_col = D3DXCOLOR(1.0f, 1.0f, 1.0f, 1.0f);	//色の初期値
}
