//=============================================================================
//HPロゴの生成
//=============================================================================
CHPLogo *CHPLogo::Create(D3DXVECTOR3 pos, float fWidth, float fHeight)
{
	CHPLogo *pStatusLogo = NULL;
	if (pStatusLogo == NULL)
	{
		pStatusLogo = new CHPLogo;		//シーンの動的確保
		if (pStatusLogo != NULL)
		{
			pStatusLogo->SetPos(pos);				//位置の設置
			pStatusLogo->SetWidth(fWidth);			//幅の設置
			pStatusLogo->SetHeight(fHeight);		//高さの設置
			pStatusLogo->BindTexture(m_pTexture);	//テクスチャ
			pStatusLogo->Init();					//初期化処理
		}
	}
	return pStatusLogo;					//値を返す
}

//=============================================================================
// オブジェクトのテクスチャ読み込み
//=============================================================================
HRESULT CHPLogo::Load(void)
{
	//レンダリングの取得
	CRenderer *pRenderer;
	pRenderer = CManager::GetRenderer();

	//デバイスの取得
	LPDIRECT3DDEVICE9 pDevice;
	pDevice = pRenderer->GetDevice();

	//テクスチャの読み込み
	D3DXCreateTextureFromFile(pDevice,
		"data\\TEXTURE\\UI_HP.png",
		&m_pTexture);

	return S_OK;
}

//=============================================================================
// オブジェクトのテクスチャ破棄
//=============================================================================
void CHPLogo::Unload(void)
{
	// テクスチャの破棄
	if (m_pTexture != NULL)
	{
		m_pTexture->Release();
		m_pTexture = NULL;
	}
}

